DROP TABLE IF EXISTS "action";
DROP TABLE IF EXISTS "command";
DROP TABLE IF EXISTS "golem";
DROP TABLE IF EXISTS "player";
DROP TABLE IF EXISTS "saved";
DROP TABLE IF EXISTS "screen";

CREATE TABLE "action" ("id" INTEGER PRIMARY KEY  AUTOINCREMENT  NOT NULL  UNIQUE , "type" VARCHAR NOT NULL , "screen_id" INTEGER, "golem_id" INTEGER, "property" VARCHAR, "value" TEXT);
CREATE TABLE "command" ("id" INTEGER PRIMARY KEY  AUTOINCREMENT  NOT NULL  UNIQUE , "screen_id" INTEGER, "action_id" INTEGER NOT NULL );
CREATE TABLE "golem" ("id" INTEGER PRIMARY KEY  AUTOINCREMENT  NOT NULL  UNIQUE , "name" VARCHAR NOT NULL  UNIQUE , "path" VARCHAR NOT NULL  UNIQUE );
CREATE TABLE "player" ("id" INTEGER PRIMARY KEY  AUTOINCREMENT  NOT NULL  UNIQUE , "username" VARCHAR NOT NULL  UNIQUE , "password" VARCHAR NOT NULL );
CREATE TABLE "saved" ("id" INTEGER PRIMARY KEY  AUTOINCREMENT  NOT NULL  UNIQUE , "name" VARCHAR NOT NULL , "player_id" INTEGER NOT NULL , "golem_id" INTEGER NOT NULL , "path" VARCHAR NOT NULL  UNIQUE );
CREATE TABLE "screen" ("id" INTEGER PRIMARY KEY  AUTOINCREMENT  NOT NULL  UNIQUE , "message" TEXT NOT NULL );
